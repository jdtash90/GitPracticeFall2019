package day1;

public class LoopPractice {
    public static void main(String[] args) {

        // random code
        for (int x = 0; x <10 ; x+=2) {
            System.out.println("x = " + x);
            for (int i = 0; i <10 ; i++) {
                System.out.println("i = " + i);
                
            }
        }
    }
}
